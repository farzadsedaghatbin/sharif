public enum FileStatus {

    RECEIVED(Direction.IN),
    REJECTED(Direction.IN),
    ACCEPTED(Direction.IN);
    Direction direction;

    FileStatus(Direction direction) {
        this.direction = direction;
    }

    public Direction getDirection() {
        return direction;
    }
}